    private void inserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirActionPerformed
      if (cpfTxt.getText().equals("")||
          nomeTxt.getText().equals("")||    
          dataNascimentoTxt.getText().equals("")||    
          pesoTxt.getText().equals("")||    
          alturaTxt.getText().equals(""))
      {   
          JOptionPane.showMessageDialog(null , "Preencher todos os campos");
      } else {
          String nome = nomeTxt.getText();
          String cpf = cpfTxt.getText();
          String dataNascimento = dataNascimentoTxt.getText();
          double peso = Double.parseDouble(pesoTxt.getText());
          int altura = Integer.parseInt(alturaTxt.getText());
          Aluno aluno = new Aluno(cpf,nome,dataNascimento,peso,altura);
          new AlunoDAO().adiciona(aluno);
          JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso!");
          nomeTxt.setText("");
          cpfTxt.setText("");
          dataNascimentoTxt.setText("");
          pesoTxt.setText("");
          alturaTxt.setText("");
      }   
    }//GEN-LAST:event_inserirActionPerformed

